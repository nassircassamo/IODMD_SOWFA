%% Installation
% The version 0.5 of the Predictor-Based Subspace IDentification Toolbox is
% recommended with Matlab 7.11 (R2010B). Although older Matlab versions
% should also work. The m-files should work on all platforms. The mex-files
% can be compiled under Windows, Mac and Linux, other platforms have not
% been checked.
%
% * Download and unzip the PBSIDToolbox-0.5.zip in a directory. If you
%  upgrade from an older version, first empty the previous directory.
%
% * Run |pbsidsetup| to add the directory to the path and compile the mex
% files. Save the path for the next start of Matlab.
%
% * Restart Matlab. From the Start button, select Toolboxes >
% Predictor-Based Subspace Identification Toolbox to check that the toolbox
% is correctly recognized by your system.
%
% * If you upgrade from an earlier version, type rehash toolbox to make
% sure that Matlab refreshes the function cache.
%
% * Run the examples to look if the functions are running correctly.
%


